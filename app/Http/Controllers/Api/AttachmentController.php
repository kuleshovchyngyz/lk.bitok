     * @return \Illuminate\Http\Response
     */
    public function deactivate(Request $request)
    {
        $attachmentsArray = array_filter(explode(',',$request->get('attachments')));
        Attachment::whereIn('id',$attachmentsArray)->update(['active'=>0]);
        return response()->noContent();
    }
    public function activate(Request $request)
    {
        $attachmentsArray = array_filter(explode(',',$request->get('attachments')));
        Attachment::whereIn('id',$attachmentsArray)->update(['active'=>1]);
        return response()->noContent();
    }

